--Why use a Lua script?
--    Without Lua, you might do:
--        Long current = redisTemplate.opsForValue().increment(key);
--        if (current == 1) {
--        redisTemplate.expire(key, 60, TimeUnit.SECONDS);
--        }
--This is not atomic. Imagine two clients:
--    Client A runs INCR and gets 1. Before Client A can call EXPIRE, the application crashes. The key is left with no TTL and never expires.
--    By putting both commands in a Lua script, Redis executes them as one atomic unit, so the counter and its expiry are always set together.


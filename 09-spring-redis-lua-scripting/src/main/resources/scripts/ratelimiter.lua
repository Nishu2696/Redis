--This is one of the most famous Redis Lua scripts. It is used to safely release a distributed lock.
--Suppose your lock key is
--    lock:product:101
--        Its value is abc123
--            where abc123 is a unique token (UUID) generated by the client that acquired the lock.
--
--Step 1
--    redis.call('GET',KEYS[1])
--    Executes --> GET lock:product:101
--    Suppose Redis returns abc123
--Step 2 == ARGV[1]
--    Suppose ARGV[1] = abc123
--    So GET == abc123
--    is true.
--
--Step 3
--    return redis.call('DEL',KEYS[1])
--    Runs DEL lock:product:101
--    Deletes the lock.
--    Returns 1
--Else Suppose Redis contains xyz999 but your application sends abc123 Then abc123 != xyz999
--    So return 0 Nothing is deleted.
--Why not simply do DEL?
--    Imagine this sequence:
--        Thread A acquires lock
--        Value = token-A TTL = 30 sec
--        Thread A starts working. It takes longer than 30 seconds. The lock expires automatically.
--        Now: lock key is gone Thread B acquires the same lock.
--        Value = token-B Thread B is now the rightful owner.
--
--    Thread A finally finishes and blindly executes: redisTemplate.delete("lock:product:101");
--    It deletes Thread B's lock, even though Thread A no longer owns it.
--
--    Now another thread could acquire the lock while Thread B is still working, breaking mutual exclusion.
--    Using the Lua script prevents this:
--
--    GET lock:product:101
--        returns token-B
--        Thread A sends token-A
--        Comparison fails. token-B != token-A
--        Redis does not delete the key.
--        Thread B keeps its lock safely.
--
--    Why is this Lua script considered "better"? A common but incorrect approach is:
--        if (redisTemplate.opsForValue().get(key).equals(token)) {
--        redisTemplate.delete(key);
--        }
--    The problem is that GET and DEL are two separate Redis commands. A race condition can occur:
--        Thread A executes GET and sees token-A.
--        Before it sends DEL, the lock expires.
--        Thread B acquires the lock with token-B.
--        Thread A executes DEL, accidentally deleting Thread B's lock.
--
--    The Lua script combines GET and DEL into a single atomic operation inside Redis.
--    No other client can modify the key between those two commands, making it the correct way to release a distributed lock.